-- ====================================================
-- ЧАСТЬ 2. ТОП-5 менторов за последний полный месяц
-- ====================================================

WITH last_month AS (
    -- Последний полный месяц в данных
    SELECT MAX(DATE_TRUNC('month', session_date_time)) AS month
    FROM sessions
    WHERE session_date_time <= CURRENT_DATE
),
top_mentors AS (
	-- Топ-менторы по числу сессий за последний месяц
    SELECT
        s.mentor_id,
        COUNT(*) AS total_sessions,
        RANK() OVER (ORDER BY COUNT(*) DESC) AS rank
    FROM sessions s
    JOIN last_month lm ON DATE_TRUNC('month', s.session_date_time) = lm.month
    WHERE s.session_status = 'finished'
    GROUP BY s.mentor_id
)
SELECT
    mentor_id,
    total_sessions,
    rank
FROM top_mentors
WHERE rank <= 5
ORDER BY rank;

