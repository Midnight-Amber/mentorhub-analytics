-- ====================================================
-- ЧАСТЬ 1. Среднее число сессий в неделю по месяцам
-- ====================================================

WITH monthly_mentor_sessions AS (
    -- Данные по каждому ментору: месяц и количество успешных сессий
    SELECT
        mentor_id,
        DATE_TRUNC('month', session_date_time) AS month,
        COUNT(*) AS total_sessions
    FROM sessions
    WHERE session_status = 'finished'
    GROUP BY mentor_id, DATE_TRUNC('month', session_date_time)
),
month_length AS (
    -- Количество недель в каждом месяце
    SELECT DISTINCT
        DATE_TRUNC('month', session_date_time) AS month,
        EXTRACT(DAY FROM 
            (DATE_TRUNC('month', session_date_time) + INTERVAL '1 month - 1 day')
        ) / 7.0 AS weeks_in_month
    FROM sessions
)
SELECT
    TO_CHAR(mms.month, 'YYYY, Month') AS month,
    ROUND((AVG(mms.total_sessions / ml.weeks_in_month))::numeric, 2) AS avg_sessions_per_week
FROM monthly_mentor_sessions mms
JOIN month_length ml ON mms.month = ml.month
GROUP BY mms.month
ORDER BY mms.month;

