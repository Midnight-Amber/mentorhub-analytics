-- ====================================================
-- ЗАДАНИЕ 4: Самый загруженный день недели по направлениям
-- ====================================================

-- КОНТЕКСТ ЗАДАЧИ:
-- В какой день недели последнего полного месяца прошло больше всего встреч для каждого направления? 
-- Определить самый загруженный день недели для каждого направления менторства.
-- ====================================================

WITH last_month AS (
    -- Последний полный месяц в данных
    SELECT MAX(DATE_TRUNC('month', session_date_time)) AS month
    FROM sessions
    WHERE session_date_time <= CURRENT_DATE
),
daily_stats AS (
    -- Количество встреч по направлениям и дням недели за последний месяц
    SELECT
        d.name AS domain_name,
        EXTRACT(DOW FROM s.session_date_time) AS dow_number,
        TO_CHAR(s.session_date_time, 'Day') AS day_name,
        COUNT(*) AS session_count
    FROM sessions s
    LEFT JOIN domain d ON s.mentor_domain_id = d.id
    CROSS JOIN last_month lm
    WHERE DATE_TRUNC('month', s.session_date_time) = lm.month
    GROUP BY d.name, dow_number, day_name
),
ranked AS (
    -- Ранжирование дней внутри каждого направления по популярности
    SELECT
        domain_name,
        TRIM(day_name) AS day_name,
        session_count,
        ROW_NUMBER() OVER (
            PARTITION BY domain_name 
            ORDER BY session_count DESC
        ) AS rank
    FROM daily_stats
)
SELECT
    domain_name,
    day_name AS busiest_day,
    session_count
FROM ranked
WHERE rank = 1
ORDER BY domain_name;

