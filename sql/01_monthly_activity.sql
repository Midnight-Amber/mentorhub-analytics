-- ====================================================
-- ЗАДАНИЕ 1: Активные пользователи по месяцам
-- ====================================================

-- КОНТЕКСТ ЗАДАЧИ:
-- Какое количество менторов и менти взаимодействуют каждый месяц на платформе?
-- Как меняется этот показатель из месяца в месяц?
-- Важно: учитываются все сессии в расчетах. Даже если сессия не состоялась, это показывает активность.
-- ====================================================

SELECT
    TO_CHAR(DATE_TRUNC('month', session_date_time), 'YYYY, Month') AS month,
    COUNT(DISTINCT mentor_id) AS active_mentors,
    COUNT(DISTINCT mentee_id) AS active_mentees
FROM sessions
GROUP BY DATE_TRUNC('month', session_date_time)
ORDER BY DATE_TRUNC('month', session_date_time);

